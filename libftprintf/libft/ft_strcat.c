#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t r;
	size_t n;

	n = 0;
	r = 0;
	while (dest[n] != '\0')
		n++;
	while (src[r] != '\0')
	{
		dest[n] = src[r];
		r++;
		n++;
	}
	dest[n] = '\0';
	return (dest);
}
