#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t r;
	size_t m;

	m = 0;
	r = 0;
	while (s1[m] != '\0')
		m++;
	while (s2[r] != '\0' && r < n)
	{
		s1[m] = s2[r];
		r++;
		m++;
	}
	s1[m] = '\0';
	return (s1);
}
